package operators;

public class Relational {
	public static void main(String[] args) {
		// var is a keyword used to store dyanamic variables, var keyword automatically set the data depended on the value we gave.
		var a = 10;
		var b = 2;
	
		System.out.println("a == b "+(a==b));
		System.out.println("a > b "+(a>b));
		System.out.println("a >= b "+(a>=b));
		System.out.println("a >= 10 "+(a>=10));
		System.out.println("a <= b "+(a<=b));
		System.out.println("a <= 10 "+(a<=10));
		System.out.println("a != b "+(a!=b));
		System.out.println("a != 10 "+(a!=10));


		
	}

}
